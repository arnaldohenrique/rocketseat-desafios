/*
    * Typeof

    - O JS é uma linguagem francamente tipada e dinâmica
    - Variáveis não precisam ter um tipo previamente definido
    - Podemos mudar o conteúdo da variável
*/

//string
let clima1 = 'Quente' 
console.log(typeof clima1)

//number
let clima2 = 1 
console.log(typeof clima2)

//boolean
let clima3 = true
console.log(typeof clima3)

//você pode ver o tipo da variavel usando 'typeof'

//lembrando que usando const você não consegue mudar o typeof da variavel