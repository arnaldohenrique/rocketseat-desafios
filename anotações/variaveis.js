/*
    *Variáveis
    
    - Nomes simbólicos para receber algum valor
    - Atalhos de código
    - Identificadores
    - 3 palavras reservadas para criar uma variável
        >var
        >let
        >const
*/

//var
var clima1 = 'Quente' 
console.log(clima1)
clima1 = 'Frio'
console.log(clima1)

//let
let clima2 = 'Frio' 
console.log(clima2)
clima2 = 'Quente'
console.log(clima2)

//const
const clima3 = 'Chovendo' //const não muda seu valor durante a aplicação
console.log(clima3)